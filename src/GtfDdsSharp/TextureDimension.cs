namespace GtfDdsSharp;

/// <summary>
/// Specifies the dimension of a GTF texture.
/// </summary>
public enum TextureDimension : byte
{
    /// <summary>
    /// The texture is one-dimensional.
    /// </summary>
    OneDimensional = 1,
    /// <summary>
    /// The texture is two-dimensional.
    /// </summary>
    TwoDimensional = 2,
    /// <summary>
    /// The texture is three-dimensional.
    /// </summary>
    ThreeDimensional = 3
}
